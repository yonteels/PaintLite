import java.awt.*;

public class ColoredPoint {
    public Point point;
    public Color color;
    public int size;


    // Method name: ColoredPoint
    // Parameter: Point point, Color color, int size
    // constructor method which help define the point, color, and size of all dot drawn.

    public ColoredPoint(Point point, Color color, int size) {
        this.point = point;
        this.color = color;
        this.size = size;
    }
}
